import React, { useState, useEffect, useRef } from 'react';

const KanbanModal = ({ card, columnId, isOpen, onClose, onUpdate, users }) => {
  const [editedCard, setEditedCard] = useState(card);
  const [newComment, setNewComment] = useState('');
  const [showUserSuggestions, setShowUserSuggestions] = useState(false);
  const [suggestionIndex, setSuggestionIndex] = useState(0);
  const [filteredUsers, setFilteredUsers] = useState([]);
  const [mentionText, setMentionText] = useState('');
  const assigneeInputRef = useRef(null);

  // usersÍ∞Ä Î∞∞Ïó¥Ïù∏ Í≤ΩÏö∞ Í∞ùÏ≤¥ ÌòïÌÉúÎ°ú Î≥ÄÌôò
  const processedUsers = Array.isArray(users) 
    ? users.reduce((acc, userName, index) => {
        acc[`user${index + 1}`] = {
          id: `user${index + 1}`,
          name: userName,
          email: `${userName.toLowerCase()}@example.com`,
          avatar: userName.charAt(0)
        };
        return acc;
      }, {})
    : users;

  useEffect(() => {
    if (card) {
      setEditedCard(card);
    }
  }, [card]);

  const handleAssigneeChange = (e) => {
    const inputValue = e.target.value;
    console.log('ÏûÖÎ†•Í∞í:', inputValue);
    
    // ÌòÑÏû¨ Îì±Î°ùÎêú ÏÇ¨Ïö©ÏûêÎì§ Í∞ÄÏ†∏Ïò§Í∏∞
    const currentUsers = editedCard.users || [];
    console.log('ÌòÑÏû¨ ÏÇ¨Ïö©ÏûêÎì§:', currentUsers);
    
    // @ Í∏∞Ìò∏Í∞Ä Ìè¨Ìï®ÎêòÏñ¥ ÏûàÎäîÏßÄ ÌôïÏù∏
    const atIndex = inputValue.lastIndexOf('@');
    if (atIndex !== -1) {
      const searchText = inputValue.substring(atIndex + 1);
      setMentionText(searchText);
      console.log('Í≤ÄÏÉâ ÌÖçÏä§Ìä∏:', searchText);
      
      // ÏÇ¨Ïö©Ïûê ÌïÑÌÑ∞ÎßÅ (Ïù¥ÎØ∏ Îì±Î°ùÎêú ÏÇ¨Ïö©Ïûê Ï†úÏô∏)
      const filtered = Object.values(processedUsers).filter(user =>
        !currentUsers.includes(user.name) && (
          user.name.toLowerCase().includes(searchText.toLowerCase()) ||
          user.email.toLowerCase().includes(searchText.toLowerCase())
        )
      );
      
      console.log('ÌïÑÌÑ∞ÎßÅÎêú ÏÇ¨Ïö©ÏûêÎì§:', filtered);
      
      if (filtered.length > 0) {
        setFilteredUsers(filtered);
        setShowUserSuggestions(true);
        setSuggestionIndex(0);
      } else {
        setShowUserSuggestions(false);
      }
    } else {
      setShowUserSuggestions(false);
      setMentionText('');
    }
  };

  const selectUser = (user) => {
    console.log('ÏÇ¨Ïö©Ïûê ÏÑ†ÌÉùÎê®:', user);
    const currentUsers = editedCard.users || [];
    const newUsers = [...currentUsers, user.name];
    console.log('ÏÉà ÏÇ¨Ïö©Ïûê Î™©Î°ù:', newUsers);
    
    setEditedCard({ ...editedCard, users: newUsers });
    onUpdate(columnId, card.id, { ...editedCard, users: newUsers });
    setShowUserSuggestions(false);
    setMentionText('');
    
    // ÏûÖÎ†• ÌïÑÎìú ÎπÑÏö∞Í∏∞
    if (assigneeInputRef.current) {
      assigneeInputRef.current.value = '';
      assigneeInputRef.current.focus();
    }
  };

  const removeAssignee = (userName) => {
    const currentUsers = editedCard.users || [];
    const newUsers = currentUsers.filter(user => user !== userName);
    setEditedCard({ ...editedCard, users: newUsers });
    onUpdate(columnId, card.id, { ...editedCard, users: newUsers });
  };

  const handleKeyDown = (e) => {
    if (showUserSuggestions) {
      if (e.key === 'ArrowDown') {
        e.preventDefault();
        setSuggestionIndex(prev => 
          prev < filteredUsers.length - 1 ? prev + 1 : 0
        );
      } else if (e.key === 'ArrowUp') {
        e.preventDefault();
        setSuggestionIndex(prev => 
          prev > 0 ? prev - 1 : filteredUsers.length - 1
        );
      } else if (e.key === 'Enter' && filteredUsers.length > 0) {
        e.preventDefault();
        selectUser(filteredUsers[suggestionIndex]);
      } else if (e.key === 'Escape') {
        e.preventDefault();
        setShowUserSuggestions(false);
        setMentionText('');
      } else if (e.key === 'Backspace' && mentionText === '') {
        setShowUserSuggestions(false);
        setMentionText('');
      }
    }
  };

  const handleInputChange = (field, value) => {
    const updatedCard = { ...editedCard, [field]: value };
    setEditedCard(updatedCard);
    
    // Ïã§ÏãúÍ∞ÑÏúºÎ°ú Î∂ÄÎ™® Ïª¥Ìè¨ÎÑåÌä∏Ïóê ÏóÖÎç∞Ïù¥Ìä∏
    onUpdate(columnId, card.id, updatedCard);
  };

  const addComment = () => {
    if (!newComment.trim()) return;

    const newCommentObj = {
      id: Date.now(),
      text: newComment,
      author: 'ÌòÑÏû¨ ÏÇ¨Ïö©Ïûê',
      date: new Date().toISOString().split('T')[0]
    };

    const updatedCard = {
      ...editedCard,
      comments: [...(editedCard.comments || []), newCommentObj],
      stats: { 
        ...(editedCard.stats || {}), 
        comments: (editedCard.stats?.comments || 0) + 1 
      }
    };

    setEditedCard(updatedCard);
    onUpdate(columnId, card.id, updatedCard);
    setNewComment('');
  };

  const formatDate = (dateString) => {
    const date = new Date(dateString);
    return date.toLocaleDateString('ko-KR', { 
      month: 'short', 
      day: 'numeric', 
      year: 'numeric' 
    });
  };

  if (!isOpen || !card || !editedCard) return null;

  return (
    <div className="modal-overlay" onClick={onClose}>
      <div className="modal-content" onClick={(e) => e.stopPropagation()}>
        <div className="modal-header">
          <input
            type="text"
            value={editedCard.title || ''}
            onChange={(e) => handleInputChange('title', e.target.value)}
            className="modal-title-input"
            placeholder="Ïπ∏Î∞ò Ï†úÎ™©ÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
          />
          <button className="modal-close" onClick={onClose}>√ó</button>
        </div>
        
        <div className="modal-body">
          <div className="modal-main">
            <div className="modal-section">
              <label>Îã¥ÎãπÏûê:</label>
              <div className="assignee-input-container">
                {editedCard.users && editedCard.users.length > 0 ? (
                  <div className="assignee-tags-container">
                    {editedCard.users.map((assignee, index) => {
                      const userName = assignee;
                      return (
                        <div key={index} className="assignee-tag">
                          <div className="assignee-tag-content">
                            <div className="assignee-avatar">
                              {userName.charAt(0)}
                            </div>
                            <span className="assignee-name">{userName}</span>
                          </div>
                          <button 
                            className="assignee-remove"
                            onClick={() => removeAssignee(userName)}
                            title="Îã¥ÎãπÏûê Ï†úÍ±∞"
                          >
                            √ó
                          </button>
                        </div>
                      );
                    })}
                    <input
                      ref={assigneeInputRef}
                      type="text"
                      value=""
                      onChange={handleAssigneeChange}
                      onKeyDown={handleKeyDown}
                      onBlur={() => {
                        // ÏïΩÍ∞ÑÏùò ÏßÄÏó∞ÏùÑ ÎëêÏñ¥ ÌÅ¥Î¶≠ Ïù¥Î≤§Ìä∏Í∞Ä Î®ºÏ†Ä Ï≤òÎ¶¨ÎêòÎèÑÎ°ù Ìï®
                        setTimeout(() => {
                          setShowUserSuggestions(false);
                          setMentionText('');
                        }, 150);
                      }}
                      className="assignee-input-inline"
                      placeholder="Îã¥ÎãπÏûêÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî (@Î°ú Î©òÏÖò)"
                    />
                  </div>
                ) : (
                  <input
                    ref={assigneeInputRef}
                    type="text"
                    value=""
                    onChange={handleAssigneeChange}
                    onKeyDown={handleKeyDown}
                    onBlur={() => {
                      setTimeout(() => {
                        setShowUserSuggestions(false);
                        setMentionText('');
                      }, 150);
                    }}
                    className="assignee-input"
                    placeholder="Îã¥ÎãπÏûêÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî (@Î°ú Î©òÏÖò)"
                  />
                )}
                {showUserSuggestions && (
                  <div 
                    className="user-suggestions"
                    onMouseDown={(e) => e.preventDefault()} // onBlur Ïù¥Î≤§Ìä∏ Î∞©ÏßÄ
                  >
                    {filteredUsers.map((user, index) => (
                      <div
                        key={user.id}
                        className={`user-suggestion ${index === suggestionIndex ? 'selected' : ''}`}
                        onClick={() => selectUser(user)}
                      >
                        <div className="user-avatar">{user.avatar}</div>
                        <div className="user-info">
                          <div className="user-name">{user.name}</div>
                          <div className="user-email">{user.email}</div>
                        </div>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            </div>
            
            <div className="modal-section">
              <label>Ïπ∏Î∞ò ÎÇ¥Ïö©</label>
              <div className="content-field">
                <textarea
                  value={editedCard.description || ''}
                  onChange={(e) => handleInputChange('description', e.target.value)}
                  className="content-textarea"
                  placeholder="Ïπ∏Î∞ò ÎÇ¥Ïö©ÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
                />
                <div className="card-meta">
                  <span className="card-date">{formatDate(editedCard.date || new Date())}</span>
                  <div className="card-stats">
                    <span className="card-stat">üëÅÔ∏è {editedCard.stats?.views || 0}</span>
                    <span className="card-stat">üîó {editedCard.stats?.links || 0}</span>
                    <span className="card-stat">üí¨ {editedCard.stats?.comments || 0}</span>
                  </div>
                </div>
                <div className="card-tags">
                  {(editedCard.tags || []).map((tag, index) => (
                    <span key={index} className="card-tag">{tag}</span>
                  ))}
                </div>
              </div>
            </div>
          </div>
          
          <div className="modal-sidebar">
            <div className="modal-section">
              <label>ÎåìÍ∏Ä Îã¨Í∏∞</label>
              <div className="comment-input-container">
                <textarea
                  value={newComment}
                  onChange={(e) => setNewComment(e.target.value)}
                  placeholder="ÎåìÍ∏ÄÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî..."
                  className="comment-input"
                />
                <button 
                  className="comment-submit"
                  onClick={addComment}
                  disabled={!newComment.trim()}
                >
                  Îì±Î°ù
                </button>
              </div>
            </div>
            
            <div className="modal-section">
              <label>ÎåìÍ∏Ä Î™©Î°ù</label>
              <div className="comments-list">
                {(editedCard.comments || []).length === 0 ? (
                  <p className="no-comments">ÎåìÍ∏ÄÏù¥ ÏóÜÏäµÎãàÎã§.</p>
                ) : (
                  (editedCard.comments || []).map((comment) => (
                    <div key={comment.id} className="comment-item">
                      <div className="comment-header">
                        <span className="comment-author">{comment.author}</span>
                        <span className="comment-date">{formatDate(comment.date)}</span>
                      </div>
                      <div className="comment-text">{comment.text}</div>
                    </div>
                  ))
                )}
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default KanbanModal; 